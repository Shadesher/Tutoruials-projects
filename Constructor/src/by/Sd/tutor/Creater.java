package by.Sd.tutor;

public class Creater {
	private String name;
	private boolean canFly;
	
	public Creater(String name, boolean canfly) {
		this.name = name;
		this.canFly = canfly;
	}
	
	public String getName() {
		return name;
	}
	
	public boolean isCanFly() {
		return canFly;
	}

}
